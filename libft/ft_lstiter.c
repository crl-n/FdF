#include "libft.h"

void	ft_lstiter(t_list *lst, void (*f)(t_list *elem))
{
	t_list	*next;

	if (!lst || !f)
		return ;
	next = lst->next;
	f(lst);
	if (next)
		ft_lstiter(next, f);
}
